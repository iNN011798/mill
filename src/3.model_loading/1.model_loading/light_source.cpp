LightSource::LightSource(glm::vec3 position, glm::vec3 color)
    : Position(position), Color(color)
{
    // 根据基础颜色设置光照分量
    Ambient  = color * glm::vec3(0.1f);
    Diffuse  = color * glm::vec3(0.8f);
    Specular = glm::vec3(1.0f, 1.0f, 1.0f); // 高光通常是白色
    setupMesh();
}

void LightSource::setupMesh() {
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);

    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

    glBindVertexArray(VAO);
    // 位置属性
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    // 法线属性 (虽然光源立方体本身不需要法线，但共用顶点数据可以简化)
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);
}

void LightSource::SetLightUniforms(Shader& shader, const std::string& uniformName) {
    shader.use();
    shader.setVec3(uniformName + ".position", Position);
    shader.setVec3(uniformName + ".ambient",  Ambient);
    shader.setVec3(uniformName + ".diffuse",  Diffuse);
    shader.setVec3(uniformName + ".specular", Specular);
}

void LightSource::Draw(Shader& shader, const glm::mat4& view, const glm::mat4& projection) {
    shader.use();
    glm::mat4 model = glm::mat4(1.0f);
    model = glm::translate(model, Position);
    model = glm::scale(model, glm::vec3(0.1f)); // 让光源立方体小一点
    shader.setMat4("model", model);
    shader.setMat4("view", view);
    shader.setMat4("projection", projection);
    shader.setVec3("lightColor", Color); // 设置光源立方体自身的颜色

    glBindVertexArray(VAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
} 